// Función para obtener el menú según el rol
export function getMenuItems(userRole) {
  return menuConfigs[userRole] || menuConfigs.cliente
}

// Función para obtener badges dinámicos (se puede extender)
export function updateMenuBadges(userRole, badges) {
  const menu = getMenuItems(userRole)
  return menu.map(item => ({
    ...item,
    badge: badges[item.path] || item.badge
  }))
}

export default {
  getMenuItems,
  updateMenuBadges
}
